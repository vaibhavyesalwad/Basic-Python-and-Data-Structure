"""Program to slice a tuple"""
tuple1 = (1, 2, 3, 4, 5)
print(f'Slicing tuple & showing only first two items:{tuple1[0:2]}')       # slicing means accessing parts of tuple
print(f'Using negative index & showing last two items:{tuple1[-2:]}')
print(f'Showing reverse of tuple: {tuple1[::-1]}')        # reversing order for display
